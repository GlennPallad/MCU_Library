void USART1_IRQHandler(void){
	uint16_t temp;
	GPIOC_Pin13_Enable();
	GPIOC_Pin13_Low();

	if (USART_GetITStatus(USART1, USART_IT_RXNE) != 0)
	{
/* temp can only be one of :
	'0'(0), '1'(255), '2'(511), '3'(1023) */
		temp = USART_ReceiveData(USART1);
		USART_SendData(USART1, temp);
		switch (temp) {
			case '0':
				pwmIndex = 0;
				break;
			case '1':
				pwmIndex = 255;
				break;
			case '2':
				pwmIndex = 511;
				break;
			case '3':
				pwmIndex = 1023;
				break;
			default:
				pwmIndex = 0;
				USART_SendString(USART1, "\n Illegal input! \n");
		}
	}
}

void TIM3_IRQHandler(void){
	if (TIM_GetITStatus(TIM3, TIM_IT_Update) != RESET) {
		TIM3->CCR3 = pwmIndex;
		TIM_ClearITPendingBit (TIM3, TIM_IT_Update);
	}
}

